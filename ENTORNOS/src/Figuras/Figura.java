package Figuras;

public abstract class Figura  
{
	/*
	 * *
	 * Devuelve el area de la figura en metros caudrados 
	 * 
	 * @return		el area de la figura en metro cuadrados
	 */
	public abstract double area ();
	/**
	 * Devuelve el perimetor de la figura en metro
	 * 
	 * @return	el perimetro de la figura en metro
	 */
	public abstract double perimetro ();
}